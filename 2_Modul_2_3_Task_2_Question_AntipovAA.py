import turtle as t  # Импортируем модуль " turtle " и присваиваем ему " t " для удобства

t.ht()  
t.pensize(5)  # Устанавливаем толщину пера
t.speed(0)  # Устанавливаем максимальную скорость рисования
t.colormode(255)  # Закомментировать для работы в " turtle " для корректной работы кода

for r in range(40, 0, -10):  # Внешний цикл по радиусам: 40, 30, 20, 10 (Старт: 40, Стоп: 0 (не включается), Шаг: -10 (уменьшение))
    for i in range(6):  # Вложенный цикл для 6 повторений каждого радиуса
        t.color(255, 165, r * 6)  # Устанавливаем цвет линии (контура)
        t.fillcolor(162, r * 6, 255)  # Устанавливаем цвет заливки фигур
        
        t.begin_fill()  # Начинаем заливку фигуры
        
  # Рисуем треугольник
        for i in range(3):  # Вложенный цикл для 3 сторон равностороннего треугольника
            t.forward(2 * r)  # Двигаемся на длину стороны (диаметр круга)
            t.right(120)  # Поворачиваем на 120 градусов для угла треугольника (360/3 = 120)
            
        t.end_fill()  # Заканчиваем заливку фигуры
        t.rt(60)  # Поворачиваем на 60 градусов

t.done()  # Для завершения работы с графическим окном.
