import turtle as t  # Импортируем модуль turtle и присваиваем значение t

t.speed(0)  # Устанавливаем максимальную скорость рисования
t.pensize(2)  # Устанавливаем толщину пера 2 пикселя
t.colormode(255)  # Устанавливаем цветовой режим RGB (0-255)
t.color("green")  # Устанавливаем цвет заливки - зеленый

# Рисуем первые 2 квадрата с перемещением между ними
for i in range(2):  # Цикл для 2 квадратов: i = 0, 1
    t.begin_fill()  # Начинаем заливку фигуры
    for j in range(4):  # Цикл для 4 сторон квадрата
        t.forward(80)  # Рисуем сторону квадрата длиной 80 пикселей
        t.left(90)  # Поворачиваем на 90 градусов влево
    t.end_fill()  # Заканчиваем заливку квадрата
    
    # Перемещаемся к следующему квадрату
    t.penup()  # Поднимаем перо (перестаем рисовать)
    t.forward(110)  # Двигаемся вперед на 110 пикселей (80 (сторона квадрата) + 30 (расстояние между квадратами))
    t.pendown()  # Опускаем перо (начинаем рисовать)

# Рисуем последний квадрат без перемещения после него
t.begin_fill()  # Начинаем заливку для последнего квадрата
for i in range(4):  # Цикл для 4 сторон квадрата
    t.forward(80)  # Рисуем сторону длиной 80 пикселей
    t.left(90)  # Поворачиваем на 90 градусов влево
t.end_fill()  # Заканчиваем заливку квадрата

t.ht()  # Скрываем черепашку (стрелочку)

t.done()  # Завершаем работу turtle (окно не закрывается сразу)
